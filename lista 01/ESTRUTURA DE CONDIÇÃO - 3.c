#include <stdio.h>
#include <stdlib.h>

/*Faça um programa que leia um número inteiro e verifique se esse número é par ou
ímpar.*/

 int main (){
    int num1=0;

    printf("Digite um numero: ");
    scanf("%d", & num1);

    if(num1 % 2 == 0){
        printf("O numero e par!");
    }
    else{
        printf("O numero e impar!");
    };
    return 0;
 }
